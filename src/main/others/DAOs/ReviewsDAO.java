package DAOs;//package DAOs;

import Annotations.Entity;
import Annotations.ForeighKey;
import Annotations.PrimaryKey;
import Annotations.Property;

import java.sql.Time;

@Entity(tableName = "Reviews")
public class ReviewsDAO {

    //Fields
    @PrimaryKey( autoIncrement =  true)
    @Property(fieldName = "Review_ID")
    private Integer ReviewD = 9;

    @ForeighKey(ReferencingTableName = "Reviews", ReferencedTableName = "Users", PrimaryKey = "id")
    @Property(fieldName = "ID")
    private Integer ID;

    @Property(fieldName = "content")
    private String content;

    @Property(fieldName = "rating")
    private int rating;

    @Property(fieldName = "Time")
    private Time time;

    @ForeighKey(ReferencingTableName = "Reviews", ReferencedTableName = "Reviews", PrimaryKey = "Review_ID")
    @Property(fieldName = "ReplyToReview")
    private int ReplyToReview;

    @ForeighKey(ReferencingTableName = "Reviews", ReferencedTableName = "Location", PrimaryKey = "Location_ID")
    @Property(fieldName = "Location_ID")
    private int LocationID;


    //Getters and Setters
    public Integer getReviewD() {
        return ReviewD;
    }

    public void setReviewD(Integer reviewD) {
        ReviewD = reviewD;
    }

    public Integer getID() {
        return ID;
    }

    public void setID(Integer ID) {
        this.ID = ID;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public Time getTime() {
        return time;
    }

    public void setTime(Time time) {
        this.time = time;
    }

    public int getReplyToReview() {
        return ReplyToReview;
    }

    public void setReplyToReview(int replyToReview) {
        ReplyToReview = replyToReview;
    }

    public int getLocationID() {
        return LocationID;
    }

    public void setLocationID(int locationID) {
        LocationID = locationID;
    }

    public ReviewsDAO()
    {

    }


    public ReviewsDAO(int ReviewID, int ID, String content, int rating, int Location_Id, Time time, int ReplyToReview)
    {
        setReviewD(ReviewID);
        setID(ID);
        setContent(content);
        setRating(rating);
        setLocationID(Location_Id);
        setTime(time);
        setReplyToReview(ReplyToReview);
    }

}

